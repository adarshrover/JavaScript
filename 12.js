//booleans & comparison operator


//booleans 
//true,false
let num1=5;
let num2=7;
let num3="5";

console.log(num1>num2);
console.log(num1<num2);

//== vs ===
console.log(num1==num2); //check whether value is same or not
console.log(num1===num3); //check whether datatype and value both are same or not


//!= vs !==
console.log(num1!=num2);
console.log(num1!==num3);
